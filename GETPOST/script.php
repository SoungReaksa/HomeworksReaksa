<?php
// Indexed Array
$fruits = ["Apple", "Banana", "Orange"];
echo $fruits[1]; // លទ្ធផល: Banana

// Associative Array
$student = ["name" => "Sok", "age" => 20];
echo $student["name"]; // លទ្ធផល: Sok

// Multidimensional Array
$classes = [
    ["Sok", "Srey"],
    ["Dara", "Vicheka"]
];
echo $classes[1][0]; // លទ្ធផល: Dara
?>